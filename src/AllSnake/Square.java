package AllSnake;

public class Square
{
    int x;
    int y;

    public Square(int newX, int newY)
    {
        this.x = newX;
        this.y = newY;
    }

    public void setX(int newX)
    {
        this.x = newX;
    }
    public int getX()
    {
        return this.x;
    }

    public void setY(int newY)
    {
        this.y = newY;
    }
    public int getY()
    {
        return this.y;
    }
}
